/*题目内容：从键盘任意输入一个3位整数，编程计算并输出它的逆序数（忽略整数前的正负号）。例如：输入-123，则忽略负号，由123分离出百位1，十位2，各位3，然后计算3*100+2*10+1=321，并输出321.


输入格式:"%d"


输出格式："%d"


输入样例：357


输出样例：753

时间限制：500ms内存限制：32000kb*/
#include<stdio.h>
int main() {
  int a,ge,shi,bai;

  scanf("%d", &a);
  a=abs(a);
  ge=a%10;
  shi=a/10%10;
  bai=a/100;
  printf("%d",ge*100+shi*10+bai);
  
  
}